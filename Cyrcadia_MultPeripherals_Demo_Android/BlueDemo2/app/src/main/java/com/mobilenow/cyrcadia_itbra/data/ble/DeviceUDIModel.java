package com.mobilenow.cyrcadia_itbra.data.ble;


public class DeviceUDIModel {
    private String recorder;
    private String leftPatch;
    private String rightPatch;

    public String getRecorder() {
        return recorder;
    }

    public void setRecorder(String recorder) {
        this.recorder = recorder;
    }

    public String getLeftPatch() {
        return leftPatch;
    }

    public void setLeftPatch(String leftPatch) {
        this.leftPatch = leftPatch;
    }

    public String getRightPatch() {
        return rightPatch;
    }

    public void setRightPatch(String rightPatch) {
        this.rightPatch = rightPatch;
    }
}
